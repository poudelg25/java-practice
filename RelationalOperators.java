public class RelationalOperators {
    public static void main(String[] args){

        float num1 = 4.2f, num2 = 2.4f;

        System.out.println(num1+">"+num2+" = "+(num1>num2));
        System.out.println(num1+"<"+num2+" = "+(num1<num2));
        System.out.println(num1+">="+num2+" = "+(num1>=num2));
        System.out.println(num1+"<="+num2+" = "+(num1<=num2));
        System.out.println(num1+"=="+num2+" = "+(num1==num2));
        System.out.println(num1+"!="+num2+" = "+(num1!=num2));

        char char1 = 'A', char2 = 'a'; //Assignment
        System.out.println(char1+" is equal to "+char2+" = "+(char1==char2));
        System.out.println(char1+" is not equal to "+char2+" = "+(char1!=char2));
    }
}
